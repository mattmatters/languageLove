package main

import "fmt"

func main() {
	fmt.Println("go" + "lang")

	// Operators
	fmt.Println("1 + 1 = ", 1 + 1)
	fmt.Println("1 - 1 = ", 1 - 1)
	fmt.Println("1 / 1 = ", 1 / 1)
	fmt.Println("1 * 1 = ", 1 * 1)
	fmt.Println("1 % 1 = ", 1 % 1)
	fmt.Println("7.0 / 3.0", 7.0 / 3.0)

	// Bitwise
	fmt.Println("true && false = ", true && false)
	fmt.Println("true || false = ", true || false)
	fmt.Println("!true = ", !true)
}
